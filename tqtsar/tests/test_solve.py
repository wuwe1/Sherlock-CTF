def test_solve(a, Setup, Boom, Fundraising):
    s = Setup.deploy({"from": a[0], "value": 1000e9})
    ins = Fundraising.at(s.instance())
    ins.fund({"from": a[1], "value": 1000e9, "gas_limit": 12000000})
    ins.fundAs(a[2], {"from": a[1], "value": 500e9, "gas_limit": 12000000})
    ins.refundInvalid(a[2], {"from": a[1], "gas_limit": 12000000})
    ins.fundAs(a[2], {"from": a[1], "value": 500e9, "gas_limit": 12000000})
    ins.refundInvalid(a[2], {"from": a[1], "gas_limit": 12000000})
    ins.fundAs(a[2], {"from": a[1], "value": 500e9, "gas_limit": 12000000})
    ins.refundInvalid(a[2], {"from": a[1], "gas_limit": 12000000})
    ins.fundAs(a[2], {"from": a[1], "value": 500e9, "gas_limit": 12000000})
    ins.refundInvalid(a[2], {"from": a[1], "gas_limit": 12000000})
    ins.repent({"from": a[1], "gas_limit": 12000000})
    assert s.isSolved()
